int		ft_atoi(char *str)
{
	int i;
	int nb;
	int sign;

	sign = 1;
	i = 0;
	nb = 0;
	while (str[i] < 33 && str[i])
		i = i + 1;
	if (str[i] == '-')
		sign = -1;
	if (str[i] == '+')
		sign = 1;
	if (str[i] == '+' || str[i] == '-')
		i = i + 1;
	while (str[i] >= 48 && str[i] <= 57)
	{
		nb = nb * 10 + (str[i] - 48);
		i = i + 1;
	}
	return (sign * nb);
}

char	*ft_rot42(char *str)
{
	int i;
	int i42;

	i = 0;
	i42 = 42;
	while (str[i])
	{
		while (i42)
		{
			if (str[i] < 127)
				str[i] = str[i]++;
			else
				str[i] = 0;
			i42 = i42 - 1;
		}
	}
	return (str);
}
